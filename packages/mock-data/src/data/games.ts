/**
 * Kategoriye göre oyunları filtreler
 */
export function getGamesByCategory(categoryType: GameCategoryType): Game[] {
  return mockGames.filter((game) => game.category === categoryType);
}

/**
 * Sınıf seviyesine göre oyunları filtreler
 */
export function getGamesByGrade(gradeLevel: number): Game[] {
  return mockGames.filter((game) => game.gradeLevel === gradeLevel);
}

/**
 * Popüler oyunları döndürür
 */
export function getPopularGames(limit: number = 10): Game[] {
  return mockGames
    .filter((game) => game.isPopular)
    .sort((a, b) => b.playCount - a.playCount)
    .slice(0, limit);
}

/**
 * Öne çıkan oyunları döndürür
 */
export function getFeaturedGames(limit: number = 5): Game[] {
  return mockGames.filter((game) => game.isFeatured).slice(0, limit);
}

/**
 * Yeni oyunları döndürür
 */
export function getNewGames(limit: number = 5): Game[] {
  return mockGames
    .filter((game) => game.isNew)
    .sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime())
    .slice(0, limit);
}
